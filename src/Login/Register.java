package Login;

public class Register {
    String id;
    String password;
    String birth;

    public String getId() {
        return id;
    }

    public String getPassword() {
        return password;
    }

    public String getBirth() {
        return birth;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public void setBirth(String birth) {
        this.birth = birth;
    }
}
